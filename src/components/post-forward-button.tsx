'use client';

import * as React from 'react';
import { Loader2, Users } from 'lucide-react';
import { useAuth } from '@/components/auth-provider';
import { useFirestore } from '@/firebase';
import { useToast } from '@/hooks/use-toast';
import { Avatar, AvatarFallback, AvatarImage } from '@/components/ui/avatar';
import { Button } from '@/components/ui/button';
import { Dialog, DialogContent, DialogHeader, DialogTitle } from '@/components/ui/dialog';
import type { Post, UserProfile } from '@/types';
import { addDoc, collection, doc, getDoc, onSnapshot, query, serverTimestamp, setDoc, where } from 'firebase/firestore';

type ChatItem = {
  id: string;
  participantIds: string[];
  updatedAt: string;
  title?: string;
  isGroup?: boolean;
};

const toIsoDate = (value: unknown) => {
  if (value && typeof value === 'object' && 'toDate' in (value as { toDate?: unknown }) && typeof (value as { toDate?: () => Date }).toDate === 'function') {
    return (value as { toDate: () => Date }).toDate().toISOString();
  }

  return new Date().toISOString();
};

export function PostForwardButton({
  post,
  className,
  iconClassName,
  stopPropagation,
  onForwarded,
}: {
  post: Post;
  className?: string;
  iconClassName?: string;
  stopPropagation?: boolean;
  onForwarded?: () => void;
}) {
  const firestore = useFirestore();
  const { user } = useAuth();
  const { toast } = useToast();

  const [open, setOpen] = React.useState(false);
  const [sendingChatId, setSendingChatId] = React.useState<string | null>(null);
  const [chats, setChats] = React.useState<ChatItem[]>([]);
  const [profilesById, setProfilesById] = React.useState<Record<string, UserProfile>>({});

  React.useEffect(() => {
    if (!open || !firestore || !user) {
      setChats([]);
      return;
    }

    const chatsQuery = query(collection(firestore, 'chats'), where('participantIds', 'array-contains', user.uid));

    const unsubscribe = onSnapshot(chatsQuery, (snapshot) => {
      const nextChats = snapshot.docs
        .map((chatDoc) => {
          const data = chatDoc.data();
          return {
            id: chatDoc.id,
            participantIds: data.participantIds || [],
            updatedAt: toIsoDate(data.updatedAt),
            title: data.title || '',
            isGroup: Boolean(data.isGroup),
          } as ChatItem;
        })
        .sort((a, b) => new Date(b.updatedAt).getTime() - new Date(a.updatedAt).getTime());

      setChats(nextChats);
    });

    return () => unsubscribe();
  }, [open, firestore, user]);

  React.useEffect(() => {
    if (!firestore || chats.length === 0) {
      setProfilesById({});
      return;
    }

    let cancelled = false;

    const fetchProfiles = async () => {
      const partnerIds = Array.from(
        new Set(
          chats
            .map((chat) => chat.participantIds.find((id) => id !== user?.uid))
            .filter((id): id is string => Boolean(id))
        )
      );

      const pairs = await Promise.all(
        partnerIds.map(async (id) => {
          try {
            const profileDoc = await getDoc(doc(firestore, 'users', id));
            if (!profileDoc.exists()) {
              return null;
            }

            const data = profileDoc.data();
            const profile: UserProfile = {
              id,
              nickname: data.nickname || 'Пользователь',
              profilePictureUrl: data.profilePictureUrl ?? null,
              createdAt: toIsoDate(data.createdAt),
              followingUserIds: data.followingUserIds || [],
              followerUserIds: data.followerUserIds || [],
            };

            return [id, profile] as const;
          } catch {
            return null;
          }
        })
      );

      if (cancelled) {
        return;
      }

      setProfilesById(Object.fromEntries(pairs.filter((pair): pair is readonly [string, UserProfile] => Boolean(pair))));
    };

    void fetchProfiles();

    return () => {
      cancelled = true;
    };
  }, [firestore, chats, user?.uid]);

  const handleForwardPost = async (chatId: string) => {
    if (!firestore || !user) {
      return;
    }

    setSendingChatId(chatId);

    try {
      await addDoc(collection(firestore, 'chats', chatId, 'messages'), {
        senderId: user.uid,
        text: '',
        imageUrls: [],
        forwardedMessage: null,
        forwardedMessages: null,
        forwardedPost: {
          postId: post.id,
          caption: post.caption || '',
          mediaUrls: post.mediaUrls || [],
          mediaTypes: post.mediaTypes || [],
          authorId: post.userId,
          likedBy: post.likedBy || [],
        },
        createdAt: serverTimestamp(),
        readBy: [user.uid],
      });

      await setDoc(
        doc(firestore, 'chats', chatId),
        {
          updatedAt: serverTimestamp(),
          lastMessageText: '↪ Переслан пост',
          lastMessageSenderId: user.uid,
        },
        { merge: true }
      );

      toast({ title: 'Пост переслан' });
      setOpen(false);
      onForwarded?.();
    } catch (error: any) {
      toast({
        title: 'Не удалось переслать пост',
        description: error?.message || 'Попробуйте ещё раз',
        variant: 'destructive',
      });
    } finally {
      setSendingChatId(null);
    }
  };

  return (
    <>
      <button
        type="button"
        className={className || 'p-1.5 text-muted-foreground transition-colors hover:text-primary'}
        onPointerDown={(event) => {
          if (stopPropagation) {
            event.stopPropagation();
            event.preventDefault();
          }
        }}
        onClick={(event) => {
          if (stopPropagation) {
            event.stopPropagation();
          }
          setOpen(true);
        }}
        aria-label="Переслать пост"
      >
        <svg
          xmlns="http://www.w3.org/2000/svg"
          viewBox="0 0 24 24"
          className={iconClassName || 'h-4 w-4'}
          aria-hidden="true"
        >
          <path
            fill="currentColor"
            d="M6.58900253,18.0753965 C6.5573092,17.8852365 6.54138127,17.692783 6.54138127,17.5 C6.54138127,15.5670034 8.10838464,14 10.0413813,14 L12.5,14 C12.7761424,14 13,14.2238576 13,14.5 L13,17.7267988 C13,17.8027316 13.0270017,17.8761901 13.0761794,17.9340463 C13.190639,18.0687047 13.3925891,18.085079 13.5272475,17.9706194 L20.5626572,11.9905211 C20.6161112,11.9450852 20.6657995,11.8953969 20.7112354,11.8419429 C21.1762277,11.2948932 21.1097069,10.4744711 20.5626572,10.0094789 L13.5272475,4.02938061 C13.4693913,3.98020285 13.3959328,3.95320119 13.32,3.95320119 C13.1432689,3.95320119 13,4.09647007 13,4.27320119 L13,7.5 C13,7.77614237 12.7761424,8 12.5,8 L9.5,8 C5.91014913,8 3,10.9101491 3,14.5 C3,17.3494045 4.26637093,19.0973664 6.88288761,19.8387069 L6.58900253,18.0753965 Z M10.0413813,15 C8.66066939,15 7.54138127,16.1192881 7.54138127,17.5 C7.54138127,17.6377022 7.55275836,17.7751689 7.57539646,17.9109975 L7.99319696,20.4178005 C8.0506764,20.7626772 7.74549866,21.0585465 7.40256734,20.990415 C3.83673227,20.2819767 2,18.0778979 2,14.5 C2,10.3578644 5.35786438,7 9.5,7 L12,7 L12,4.27320119 C12,3.54418532 12.5909841,2.95320119 13.32,2.95320119 C13.6332228,2.95320119 13.9362392,3.06458305 14.1748959,3.26744129 L21.2103057,9.24753957 C22.1781628,10.0702182 22.2958533,11.5217342 21.4731747,12.4895914 C21.3927882,12.5841638 21.3048781,12.6720739 21.2103057,12.7524604 L14.1748959,18.7325587 C13.6194301,19.2047047 12.7863861,19.1371606 12.3142401,18.5816947 C12.1113819,18.343038 12,18.0400216 12,17.7267988 L12,15 L10.0413813,15 Z"
          />
        </svg>
      </button>

      <Dialog open={open} onOpenChange={setOpen}>
      <DialogContent
        className="max-w-md"
        onPointerDown={(event) => event.stopPropagation()}
        onClick={(event) => event.stopPropagation()}
      >
        <DialogHeader>
          <DialogTitle>Переслать пост</DialogTitle>
        </DialogHeader>

        {!user ? (
          <p className="text-sm text-muted-foreground">Войдите, чтобы переслать пост в личные сообщения.</p>
        ) : chats.length === 0 ? (
          <p className="text-sm text-muted-foreground">Нет доступных диалогов для пересылки.</p>
        ) : (
          <div className="max-h-[60vh] space-y-2 overflow-y-auto pr-1">
            {chats.map((chat) => {
              const isGroupChat = Boolean(chat.isGroup || chat.participantIds.length > 2);
              const partnerId = chat.participantIds.find((id) => id !== user.uid);
              const partner = partnerId ? profilesById[partnerId] : null;
              const chatName = isGroupChat ? (chat.title?.trim() || 'Беседа') : partner?.nickname || 'Диалог';
              const isSending = sendingChatId === chat.id;

              return (
                <button
                  key={chat.id}
                  type="button"
                  onClick={(event) => {
                    event.stopPropagation();
                    void handleForwardPost(chat.id);
                  }}
                  disabled={Boolean(sendingChatId)}
                  className="flex w-full items-center gap-3 rounded-lg border border-border/60 p-2 text-left transition hover:bg-muted/40 disabled:cursor-not-allowed"
                >
                  <Avatar className="h-9 w-9">
                    {isGroupChat ? (
                      <AvatarFallback>
                        <Users className="h-4 w-4" />
                      </AvatarFallback>
                    ) : (
                      <>
                        <AvatarImage src={partner?.profilePictureUrl ?? undefined} alt={partner?.nickname || 'Чат'} />
                        <AvatarFallback>{(partner?.nickname || 'Ч')[0]?.toUpperCase()}</AvatarFallback>
                      </>
                    )}
                  </Avatar>
                  <div className="min-w-0 flex-1">
                    <p className="truncate text-sm font-medium">{chatName}</p>
                  </div>
                  {isSending && <Loader2 className="h-4 w-4 animate-spin" />}
                </button>
              );
            })}
          </div>
        )}

        <div className="flex justify-end">
          <Button
            type="button"
            variant="outline"
            onClick={(event) => {
              event.stopPropagation();
              setOpen(false);
            }}
          >
            Закрыть
          </Button>
        </div>
      </DialogContent>
      </Dialog>
    </>
  );
}
